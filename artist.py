class Artist:
    def __init__(
        self, artist_id, continent, genre, is_famous, number_of_streams, week_no_created
    ):
        self.artist_id = artist_id
        self.continent = continent
        self.genre = genre
        self.is_famous = is_famous
        self.number_of_streams = number_of_streams
        self.week_no_created = week_no_created
